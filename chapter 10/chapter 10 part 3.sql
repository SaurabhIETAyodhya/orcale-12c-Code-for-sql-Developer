---------- part three retriving records with USING-------------------- 
--6 
SELECT 
EMPLOYEES.EMPLOYEE_ID , 
EMPLOYEES.FIRST_NAME, 
DEPARTMENT_ID, --note you can not prefix table name in the match column
DEPARTMENTS.DEPARTMENT_NAME
